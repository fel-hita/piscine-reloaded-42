int		ft_iterative_factorial(int nb)
{
	int n;

	if (nb < 0)
		return (0);
	else if (nb == 0)
		return (1);
	else
	{
		n = 1;
		while (nb > 0)
		{
			n *= nb;
			nb--;
		}
	}
	return (n);
}
