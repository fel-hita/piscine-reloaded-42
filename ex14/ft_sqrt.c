int		ft_sqrt(int nb)
{
	int n;

	if (nb == 4)
		return (2);
	else if (nb < 9)
		return (0);
	else
	{
		n = 3;
		while (n < (nb / 3))
		{
			if ((n * n) == nb)
				return (n);
			n++;
		}
	}
	return (0);
}
