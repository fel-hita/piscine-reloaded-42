int		ft_count_if(char **tab, int (*f) (char*))
{
	int i;
	int counter;

	i = 0;
	counter = 0;
	while (tab[i])
	{
		if (f(tab[i]))
		{
			counter++;
		}
		i++;
	}
	return (counter);
}
